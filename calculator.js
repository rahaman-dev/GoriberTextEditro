// bold
document.getElementById("bold-btn").addEventListener("click", function () {
  let getInputField = document.getElementById("input-field");
  let styleInputField =
    ((getInputField.style.fontWeight = "bold"),
    (getInputField.style.fontSize = "15px"));
  getInputField = styleInputField;
});

